            <h2><span class="subhead">When is Formaldehyde harmful?</span></h2>
            <p>
                Average formaldehyde levels in homes are ranging from 0.10 to 3.68 parts per million (ppm).  Newly manufactured or mobile homes are usually at the high end of the spectrum.
                The reference dose for formaldehyde is 0.2 milligrams per kilogram body weight per day.
            </p>
            <p>
                Exposure to low levels of formaldehyde can burn the eyes, throat, nose and skin. Long term exposure can cause cancer of the nose, throat and blood. High exposure to formaldehyde can cause a buildup of fluid in the lungs, bronchitis, severe shortness of breath and a rapid heart rate. People with respiratory problems like asthma may be more sensitive to exposure to formaldehyde.
            </p>
